#include "libft.h"

int	ft_isalnum(int a)
{
	if (a >= '0' && a <= '9')
		return (2);
	else if (a >= 'a' && a <= 'z')
		return (2);
	else if (a >= 'A' && a <= 'Z')
		return (2);
	return (0);
}
