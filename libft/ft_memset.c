#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	int				i;
	unsigned char	*a;

	i = 0;
	a = (unsigned char *)s;
	while (n > 0)
	{
		a[i] = c;
		i++;
		n--;
	}
	return (s);
}
